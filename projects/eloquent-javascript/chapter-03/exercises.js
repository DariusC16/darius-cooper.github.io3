// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  // USING MATH MIN METHOD
   var a = Math.min(num1, num2);

   // RETURNING SMALLEST VALUE
    return a;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  // BASE CASE //
  // TIME FOR DARIUS STYLE CODE
   // IF NUMBER IS 1
   if (n === 1) {
    // RETURN FALSE
     return false;
  } 
  // IF NUMBER IS LESS THAN 0
   if (n < 0) {
    // RECURSION ADD 2 TO NUMBER
     return isEven(n+2);
   }
  // IF NUMBER IS 0
   if (n === 0) {
    // RETURN TRUE
     return true;
   } else {
      // RECURSION SUBTRACT 2 FROM NUMBER
       return isEven(n-2);
   }  
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  // CREATING VARIABLE
   var count = 0;

   // USING FOR LOOP 
    for (var i = 0; i <= string.length - 1; i++) {
      // IF INDEX IN STRING IS CHARACTER
       if (string[i] === char) {
        // ADD 1 TO COUNT
         count += 1;
       }
    }
    // RETURN COUNT
     return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  // CREATING VARIABLE
  var count = 0;

  // USING FOR LOOP 
   for (var i = 0; i <= string.length - 1; i++) {
     // IF INDEX IN STRING IS UPPER CASE B
      if (string[i] === 'B') {
       // ADD 1 TO COUNT
        count += 1;
      }
   }
   // RETURN COUNT
    return count;
}

